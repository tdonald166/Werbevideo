// Scans configs/*.json and writes src/ads/configs.gen.ts with all imports.
// Run before each render so newly uploaded configs are picked up.
import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";

const files = (await readdir("configs"))
  .filter((f) => f.endsWith(".json"))
  .sort();

const safeName = (id) => "cfg_" + id.replace(/[^a-zA-Z0-9]/g, "_");

const imports = files.map((f) => {
  const id = path.basename(f, ".json");
  return `import ${safeName(id)} from "../../configs/${f}";`;
}).join("\n");

const list = files.map((f) => safeName(path.basename(f, ".json"))).join(", ");

const out = `// AUTO-GENERATED — do not edit. Regenerated by scripts/generate-configs-index.mjs
import type { AdConfig } from "./types";
${imports}

export const adConfigs: AdConfig[] = [${list}] as AdConfig[];
`;

await writeFile("src/ads/configs.gen.ts", out, "utf-8");
console.log(`✓ Generated src/ads/configs.gen.ts (${files.length} configs)`);
